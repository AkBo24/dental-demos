import type { ProcedureSupplyRequirement, ProcedureTemplate } from '@/types'

type Req = {
  itemId: string
  quantity: number
  unit: string
  required?: boolean
  wastePercent?: number
}

function bom(procedureTemplateId: string, reqs: Req[]): ProcedureSupplyRequirement[] {
  return reqs.map((r, i) => ({
    id: `${procedureTemplateId}-r${i + 1}`,
    procedureTemplateId,
    itemId: r.itemId,
    quantity: r.quantity,
    unit: r.unit,
    required: r.required ?? true,
    wastePercent: r.wastePercent ?? 5,
  }))
}

